  const diff = (arr, values) => {
    let newArr = [...arr];
    for (const i of values) {
      const index = newArr.indexOf(i);
      if (index > -1) {
        newArr.splice(index, 1);
      }
    }
    return newArr;
  };
  
  export const generateMotor = (motor, type) => {
    const main = ["1", "2", "3", "4", "5", "6", "7", "8", "9", "0"];
    const difference = diff(main, motor);
    let result = [...type];
    for (let item of difference) {
      for (let d of type) {
        if (d.includes(item)) {
          const index = result.indexOf(d);
          if (index > -1) {
            result.splice(index, 1);
          }
        }
      }
    }
    return result;
  };
  